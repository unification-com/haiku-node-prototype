use Datablobs;

CREATE TABLE BlobCreator (
    CID int NOT NULL,
    Name varchar(255),
    PRIMARY KEY (CID)
);

CREATE TABLE BlobData (
    BlobID int NOT NULL,
    DataBlob BLOB,
    BlobSize int,
    TimeStamp TIMESTAMP DEFAULT (STRFTIME('%d/%m/%Y', 'NOW')),
    CreatorID int,
    PRIMARY KEY (BlobID),
    FOREIGN KEY (CreatorID) REFERENCES BlobCreator(CID)
);

